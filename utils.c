#include <unistd.h>

void	ft_putnbr(int c)
{
	char *set;

	set = "0123456789";

	if (c < 0)
	{
		write(1, "-", 1);
		c *= -1;
	}

	if (c > 9)
	{
		ft_putnbr(c / 10);
		ft_putnbr(c % 10);
	}
	if (c < 9)
		write(1, &set[c], 1);
	return;
}